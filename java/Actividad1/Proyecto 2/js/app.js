//Defino las variables 
var a,b,area;

//Capturar los datos del usuario ..Entrada
a=parseFloat(prompt("Ingrese la altura del rectangulo:"));
b=parseFloat(prompt("Ingrese la base del rectangulo:"));

area=a*b;

//Mensaje de salida
document.write(" la altura de su rectangulo de: " +a+ " cm y la base de: " +b+ " cm da un area de: " +area);